package tutorial;

public class J_01_Variables_DataTypes {

	public static void main(String[] args) {
		int iNum = 10;
		System.out.println(iNum);

		double dNum = 10.13;
		System.out.println(dNum);

		boolean value = true;
		System.out.println(value);

		char letter = 'D';
		System.out.println(letter);

		String name = "Dong";
		System.out.println(name);
	}

}
